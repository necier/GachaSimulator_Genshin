# 用于绑定物品ID
def returndict(L, startid, mode=0):
    if mode == 0:
        return dict(zip([str(i) for i in range(startid, startid + len(L))], copy.deepcopy(L)))
    else:
        return dict(zip(copy.deepcopy(L), [str(i) for i in range(startid, startid + len(L))]))


